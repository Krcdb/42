#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int len;

	len = 0;
	while (*s1 != '\0')
	{
		s1++;
		len++;
	}
	while (*s2 != '\0' && n)
	{
		*s1++ = *s2++;
		len++;
		n--;
	}
	*s1 = '\0';
	s1 -= len;
	return (s1);
}
